package basics;
public class datatype{
  public static void main(String a[]){



    // integers

    // int = 4 bytes
    int i = 45;
    // long = 8 bytes;
    long l = 23545l;
    // short = 2 bytes
    short sh = 45;
    // byte = 1 byte
    byte b = 127;
    // -----------------------------------------------------------------


    // float

    // float = 4 bytes
    float f = 45.241f;
    // double = 8 (defualt)
    double d = 145.4556;
    // ----------------------------------------------------------------

    // char

    // char = 2 byte follows unicode not ascii
    char c = 'k';
    // -----------------------------------------------------------------
    
    // boolean

    // boolean = true or false not 0, 1
    boolean bool = true;
    // -----------------------------------------------------------------


  }
}